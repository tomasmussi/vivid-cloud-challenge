package com.challenge.core;

public interface LongestPalindromicFinder {

    /**
     * Find the longest palindromic number between number1 and number2
     *
     * @param number1
     * @param number2
     * @return the longest palindromic number between the bounds or -1 if no palindromic number exists.
     */
    int findLongestPalindrome(int number1, int number2);
}
